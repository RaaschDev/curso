# Ranges
# - Precisamos conhecer o loop for para usar os ranges
# - Precisamos conhecer o range para trabalhar melhor com o loop for

# Ranges sao utilizados para gerar sequencias numericas, nao de forma aleatoria e sim de maneira especificada

# Formas gerais
# range(valor_de_parada)

for num in range(20):
    print(num)
    
for num in range(5,20):
    print(num)

for num in range(5,20,5):
    print(num)